def auto_detect_mcp():
    """Try to auto-detect MCP connection details"""
    
    print("ðŸ” Attempting to auto-detect MCP connection...")
    
    # Common MCP ports and URLs
    common_urls = [
        "http://localhost:3000",
        "http://localhost:8000",
        "http://localhost:5000",
        "http://127.0.0.1:3000",
        "http://127.0.0.1:8000",
        "http://127.0.0.1:5000"
    ]
    
    for url in common_urls:
        try:
            import requests
            response = requests.get(f"{url}/health", timeout=2)
            if response.status_code == 200:
                print(f"âœ… Found potential MCP server at: {url}")
                return url
        except:
            continue
    
    print("âŒ Could not auto-detect MCP connection")
    return None

def check_current_setup():
    """Check current MCP setup"""
    
    print("ðŸ” Checking current MCP setup...")
    
    # Check for environment variables
    vars_to_check = [
        "ANTHROPIC_BASE_URL",
        "ANTHROPIC_AUTH_TOKEN", 
        "GLM_URL",
        "GLM_API_TOKEN"
    ]
    
    found_vars = {}
    for var in vars_to_check:
        value = os.getenv(var)
        if value:
            found_vars[var] = f"{value[:20]}..." if len(value) > 20 else value
    
    if found_vars:
        print("âœ… Found MCP environment variables:")
        for var, value in found_vars.items():
            print(f"   {var}: {value}")
    else:
        print("âŒ No MCP environment variables found")
    
    # Check for .env file
    env_file = Path(".env")
    if env_file.exists():
        print(f"âœ… .env file exists at {env_file}")
    else:
        print("âŒ .env file not found")
    
    # Test connection if variables are set
    if found_vars:
        print("\nðŸ§ª Testing current MCP connection...")
        try:
            from claude_mcp_client import ClaudeMCPClient
            client = ClaudeMCPClient()
            info = client.get_connection_info()
            print("âœ… MCP client can be initialized")
            print(f"Connection details: {info}")
        except Exception as e:
            print(f"âŒ MCP connection test failed: {e}")

if __name__ == "__main__":
    if len(sys.argv) > 1:
        if sys.argv[1] == "check":
            check_current_setup()
        elif sys.argv[1] == "detect":
            url = auto_detect_mcp()
            if url:
                print(f"Detected MCP server at: {url}")
            else:
                print("No MCP server detected")
    else:
        check_claude_desktop()
        setup_mcp_environment()
